
def area(a):
    """
    Computes the area of a square by its side.

    Args:
        a (number): Length of square`s side

    Returns:
        Area (number): S = a * a

    Example:
        area(5) -> 25 
    """
    return a * a


def perimeter(a):
    """
    Computes the perimeter of a square by its side.

    Args:
        a (number): Length of square`s side

    Returns:
        Perimeter (number): P = 4 * a

    Example:
        area(5) -> 20 
    """
    return 4 * a
